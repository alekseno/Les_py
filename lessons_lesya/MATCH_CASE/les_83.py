planet = input()
match planet:
    case "Земля" | "Меркурий" | "Венера" | "Марс":
        print("Это планета земной группы.")
    case "Юпитер" | "Сатурн" | "Уран" | "Нептун":
        print("Это газовый гигант.")
    case "Плутон":
        print("Это карликовая планета.") 
    case _: 
        print("Такой планеты нет в Солнечной системе.")                              